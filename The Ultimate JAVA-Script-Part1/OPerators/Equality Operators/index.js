// Strict Equality Operator
console.log(1===1);
// it will get true

// it will get false;
console.log('1'===1);


// Lose Equality Operators
console.log(1==1);
// it will get True;
// it will also get true;
console.log('1'==1);
console.log(true==1);
// it will get false;
console.log(false==1);
console.log(false==true);
console.log(true==0);